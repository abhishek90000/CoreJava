package com.basicprgms;

//example on double literal literal
class literals
{
	public static void main(String[] args)
	{
		float a1 = 5;
		float a2 = 5.0F;
		double b = 5464.40123;
		
		System.out.println("a1 contains "+a1);
		System.out.println("a2 contains "+a2);	
		System.out.println("b contains "+b);	
	}
}